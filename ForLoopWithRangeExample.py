# Example: Print numbers from 0 to 9

for i in range(10):
    print(i)


'''exlanation


range(10) generates a sequence of numbers from 0 to 9.

for i in range(10): sets up a loop that iterates over each number in the sequence.

print(i) outputs each number during each iteration of the loop.'''

# more examples Print even numbers from 0 to 18

